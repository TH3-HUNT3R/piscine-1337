unsigned	int	ft_active_bits(int value)
{
	int i;

	i = 0;
	if (i == 0)
	{
		return (0);
	}
	else
	{
		while (value > 0)
		{
			value = value / 10;
			i++;
		}
	}
	return (2 * i);
}
