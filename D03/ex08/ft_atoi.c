int	ft_atoi(char *str)
{
	int res;
	int sig;
	int i;

	res = 0;
	sig = 1;
	i = 0;
	if (*str == '-')
	{
		sig = -1;
		str++;
	}
	if (*str == '+')
		str++;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			break ;
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * sig);
}
