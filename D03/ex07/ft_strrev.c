char	*ft_strrev(char *str)
{
	int		cp;
	int		i;
	char	c;

	cp = 0;
	while (str[cp] != '\0')
	{
		cp++;
	}
	cp -= 1;
	i = 0;
	while (i < ((cp + 1) / 2))
	{
		c = str[i];
		str[i] = str[cp - i];
		str[cp - i] = c;
		i++;
	}
	return (str);
}
