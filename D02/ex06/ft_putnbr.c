void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int n;

	n = 10;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb >= 0 && nb < 10)
	{
		ft_putchar(nb + '0');
	}
	else
	{
		while (nb >= n)
		{
			n *= 10;
		}
		while (nb != 0 && n >= 10)
		{
			n /= 10;
			ft_putchar((nb / n) + '0');
			nb %= n;
		}
	}
}
